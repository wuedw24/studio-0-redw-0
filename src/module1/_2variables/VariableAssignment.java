package module1._2variables;

public class VariableAssignment
{
   public static void main(String[] args)
   {
     int score;
     score = 4;
     System.out.println(score);

     double price = 23.25;
     System.out.println(price);

     boolean won = false;
     System.out.println(won);
     won = true;
     System.out.println(won);

     String name = "Jose";
     System.out.println(name);
   }
}

