package module1._3expressions;

public class VariableAssignment
{
  public static void main(String[] args)
  {
    int x = 3;
    int y = 2;
    System.out.println(x);
    System.out.println(y);
    x = y;
    System.out.println(x);
    System.out.println(y);
    y = 5;
    System.out.println(x);
    System.out.println(y);
  }
}
