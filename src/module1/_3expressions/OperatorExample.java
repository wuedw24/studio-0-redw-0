package module1._3expressions;

public class OperatorExample
{
   public static void main(String[] args)
   {
     System.out.println(2 + 3);
     System.out.println(2 - 3);
     System.out.println(2 * 3);
     System.out.println(2 / 3);
     System.out.println(2 == 3);
     System.out.println(2 != 3);
   }
}
